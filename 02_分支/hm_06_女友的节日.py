# 定义holiday_name字符串变量记录节日名称
holiday_name = "生日"
# 如果是情人节,应该买玫瑰/看电影
# 判断要用两个=
if holiday_name == "情人节":
    print("应该买玫瑰")
    print("应该看电影")
# 如果是平安夜,应该买苹果/吃大餐
elif holiday_name == "平安夜":
    print("应该买苹果")
    print("应该吃大餐")
# 如果是生日.应该买蛋糕
elif holiday_name == "生日":
    print("应该买蛋糕")
# 其他的日子每天都是节日啊......
else:
    print("每天都是节日啊!")